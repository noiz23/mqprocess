package cl.vmetrix.xml.model;

/**
 * This class defines the object used to create the XML file final.
 * 
 * @author REcheverri
 *
 */
public class FileNameXml {
	
	private int name;
	
	private String content;

	public int getName() {
		return name;
	}

	public void setName(int name) {
		this.name = name;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
	
	

}
